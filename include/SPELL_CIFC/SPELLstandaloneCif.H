//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Standalone client interface
 **
 ** \par Description and usage:
 **
 ** 	This interface is used in SPELL Executor Command-line
 **  	process when running in non-interactive mode.
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLstandaloneCif: public SPELLcif
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor
		**////////////////////////////////////////////////////////////////////
		SPELLstandaloneCif();

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		virtual ~SPELLstandaloneCif();

	// STATIC ////////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::setup()
		**////////////////////////////////////////////////////////////////////
		void setup( const SPELLcifStartupInfo& info );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::cleanup().
		**////////////////////////////////////////////////////////////////////
		void cleanup( bool force );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::canClose()
		**////////////////////////////////////////////////////////////////////
		void canClose();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::waitClose()
		**////////////////////////////////////////////////////////////////////
		void waitClose();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::resetClose()
		**////////////////////////////////////////////////////////////////////
		void resetClose();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::getArguments()
		**////////////////////////////////////////////////////////////////////
		std::string getArguments();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::getCondition()
		**////////////////////////////////////////////////////////////////////
		std::string getCondition();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::isAutomatic()
		**////////////////////////////////////////////////////////////////////
		bool isAutomatic();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::isVisible(). Not used in this interface.
		**////////////////////////////////////////////////////////////////////
		bool isVisible() {
			return true;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::isBlocking(). Not used in this interface.
		**////////////////////////////////////////////////////////////////////
		bool isBlocking() {
			return true;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif::isHeadless(). Not used in this interface.
		**////////////////////////////////////////////////////////////////////
		bool isHeadless() {
			return true;
		};

    //====================================================================
    // Python bindings start (see SPELLcif class)
    //====================================================================

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyLine();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyCall();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyReturn();

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyStatus( const SPELLstatusInfo& st );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyError( const std::string& error, const std::string& reason, bool fatal );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyUserActionSet( const std::string& label, const unsigned int severity ) {
			;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyUserActionUnset() {
			;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notifyUserActionEnable( bool enable ) {
			;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void notify( ItemNotification notification ) {
			;
		};

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void write( const std::string& msg, unsigned int scope );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void warning( const std::string& msg, unsigned int scope );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void error( const std::string& msg, unsigned int scope );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void log( const std::string& msg );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		std::string prompt( const SPELLpromptDefinition& definition );

		//////////////////////////////////////////////////////////////////////
		/** \see SPELLcif class.
		**////////////////////////////////////////////////////////////////////
		void specificSetup( const SPELLcifStartupInfo& info );

    //====================================================================
    // Python bindings end
    //====================================================================

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // STATIC ////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////
		/** Holds the NCurses shell instance */
		SPELLncursesShell* m_shell;

};

