#ifndef __SPELL_EXECUTOR_IPC_H__
#define __SPELL_EXECUTOR_IPC_H__

// FILES TO INCLUDE //////////////////////////////////////////////////////////
// Local includes ------------------------------------------------------------
#include "SPELL_CTX/SPELLexecutorStartupParams.H"
// Project includes ----------------------------------------------------------
#include "SPELL_IPC/SPELLipcServerInterfaceSingle.H"
#include "SPELL_SYN/SPELLmutex.H"

/** \addtogroup SPELL_CTX */
/*@{*/


// FORWARD REFERENCES ////////////////////////////////////////////////////////
class SPELLexecutor;
class SPELLexecutorListener;
// TYPES /////////////////////////////////////////////////////////////////////
// DEFINES ///////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLexecutorIPC : public SPELLipcInterfaceListener
{
public: //--------------------------------------------------------------------

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLexecutorIPC( SPELLexecutor& controller, const SPELLexecutorStartupParams& config );

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		virtual ~SPELLexecutorIPC();

	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Setup the interface
		**////////////////////////////////////////////////////////////////////
		void setup();

		//////////////////////////////////////////////////////////////////////
		/** Cleanup the interface
		**////////////////////////////////////////////////////////////////////
		void cleanup();

		//////////////////////////////////////////////////////////////////////
		/** Get listening port
		**////////////////////////////////////////////////////////////////////
		int getPort();

		//====================================================================
		// OUTGOING IPC METHODS
		//====================================================================

	    //////////////////////////////////////////////////////////////////////
	    /** Send a message to an executor
	    **////////////////////////////////////////////////////////////////////
		void sendMessage( const std::string& executorId, const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** Send a request to an executor
	    **////////////////////////////////////////////////////////////////////
		SPELLipcMessage sendRequest( const std::string& executorId, const SPELLipcMessage& msg, unsigned long timeoutMsec );

		//====================================================================
		// NOTIFICATION AND CONTROL METHODS
		//====================================================================

		//////////////////////////////////////////////////////////////////////
		/** Add a given notifier
		**////////////////////////////////////////////////////////////////////
		void registerExecutorNotifier( std::string id, SPELLexecutorListener* notifier );

		//////////////////////////////////////////////////////////////////////
		/** Remove a given notifier
		**////////////////////////////////////////////////////////////////////
		void deregisterExecutorNotifier( std::string id );

	// DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

		friend class SPELLipcInterface;
		friend class SPELLexecutor;

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////
	// STATIC ////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//====================================================================
		// INTERNAL IPC METHODS
		//====================================================================

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processMessage( const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    SPELLipcMessage processRequest( const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processConnectionError( int clientKey, std::string error, std::string message );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processConnectionClosed( int clientKey );

		//====================================================================
		// NOTIFICATION
		//====================================================================

	    void notifyMessage( const SPELLipcMessage& msg );
	    void notifyRequest( const SPELLipcMessage& msg );

	    bool shouldForwardToMonitoring( const SPELLipcMessage& msg );

private: //-------------------------------------------------------------------

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////

	    typedef std::map<std::string,SPELLexecutorListener*> NotifierMap;

	// STATIC ////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////
	// DATA MEMBERS //////////////////////////////////////////////////////////

		/** Event for process start */
		SPELLevent m_executorLoggedInEvent;
		/** Event for process stop */
		SPELLevent m_executorLoggedOutEvent;
		/** Holds the server interface for executors */
		SPELLipcServerInterfaceSingle m_ipc;
		/** Holds the controller listener */
		SPELLexecutor& m_controller;
		/** Holds the notifier listener */
		NotifierMap m_notifiers;
		/** Synchronization for notifications */
		SPELLmutex m_notifyLock;
		/** Connected flag */
		bool m_connected;


};

/*@}*/
#endif
