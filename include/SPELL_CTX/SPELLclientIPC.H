#ifndef __SPELL_CLIENT_IPC_H__
#define __SPELL_CLIENT_IPC_H__

// FILES TO INCLUDE //////////////////////////////////////////////////////////
// Local includes ------------------------------------------------------------
#include "SPELL_CTX/SPELLclientListener.H"
#include "SPELL_CTX/SPELLclientInterestList.H"
// Project includes ----------------------------------------------------------
#include "SPELL_IPC/SPELLipcServerInterface.H"
#include "SPELL_SYN/SPELLmutex.H"

/** \addtogroup SPELL_CTX */
/*@{*/


// FORWARD REFERENCES ////////////////////////////////////////////////////////
// TYPES /////////////////////////////////////////////////////////////////////
// DEFINES ///////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLclientIPC : public SPELLipcInterfaceListener
{
public: //--------------------------------------------------------------------

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLclientIPC();

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		virtual ~SPELLclientIPC();

	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Setup the interface
		**////////////////////////////////////////////////////////////////////
		void setup();

		//////////////////////////////////////////////////////////////////////
		/** Cleanup the interface
		**////////////////////////////////////////////////////////////////////
		void cleanup();

		//////////////////////////////////////////////////////////////////////
		/** Get listening port
		**////////////////////////////////////////////////////////////////////
		int getPort();

		//====================================================================
		// NOTIFICATION AND CONTROL METHODS
		//====================================================================

		//////////////////////////////////////////////////////////////////////
		/** Add interest on a given client
		 *
		 * \param clientKey IN: ipc key of the client
		 *
		**////////////////////////////////////////////////////////////////////
		void registerInterest( int clientKey,
							   SPELLclientListener* listener );

		//////////////////////////////////////////////////////////////////////
		/** Remove interest on a given client
		 *
		 * \param clientKey IN: ipc key of the client
		 *
		**////////////////////////////////////////////////////////////////////
		void unregisterInterest( int clientKey,
							     SPELLclientListener* listener );

		//====================================================================
		// OUTGOING IPC METHODS
		//====================================================================

	    //////////////////////////////////////////////////////////////////////
	    /** Send a message to a client
	    **////////////////////////////////////////////////////////////////////
		void sendMessage( int ipcKey, const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** Send a request to a client
	    **////////////////////////////////////////////////////////////////////
		SPELLipcMessage sendRequest( int ipcKey, const SPELLipcMessage& msg, unsigned long timeoutMsec );

		//====================================================================
		// INTEREST LIST MANAGEMENT
		//====================================================================

		//////////////////////////////////////////////////////////////////////
		/**
		**////////////////////////////////////////////////////////////////////
	    void removeAllInterest();

		//////////////////////////////////////////////////////////////////////
		/**
		**////////////////////////////////////////////////////////////////////
	    SPELLclientInterestList* createClientInterestList( int clientKey );

		//////////////////////////////////////////////////////////////////////
		/**
		**////////////////////////////////////////////////////////////////////
		void destroyClientInterestList( int clientKey );

		//////////////////////////////////////////////////////////////////////
		/**
		**////////////////////////////////////////////////////////////////////
		SPELLclientInterestList* getClientInterestList( int clientKey );

		//////////////////////////////////////////////////////////////////////
		/**
		**////////////////////////////////////////////////////////////////////
		void cancelRequestsToClient( int clientKey );

	// DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

		friend class SPELLipcInterface;

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////
	// STATIC ////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//====================================================================
		// INTERNAL IPC METHODS
		//====================================================================

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processMessage( const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    SPELLipcMessage processRequest( const SPELLipcMessage& msg );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processConnectionError( int clientKey, std::string error, std::string message );

	    //////////////////////////////////////////////////////////////////////
	    /** \see SPELLipcInterfaceListener
	    **////////////////////////////////////////////////////////////////////
	    void processConnectionClosed( int clientKey );


private: //-------------------------------------------------------------------

	// EXCEPTIONS ////////////////////////////////////////////////////////////
	// ENUMS /////////////////////////////////////////////////////////////////
	// TYPES /////////////////////////////////////////////////////////////////

		typedef std::map<int,SPELLclientInterestList*> InterestMap;

	// STATIC ////////////////////////////////////////////////////////////////
	// LIFECYCLE /////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////
	// DATA MEMBERS //////////////////////////////////////////////////////////

		/** Holds the server interface for clients */
		SPELLipcServerInterface m_ipc;
		/** Holds the set of interest lists per executor */
		InterestMap					m_interestMap;
		/** Synchronization lock for interest lists */
		SPELLmutex					m_interestLock;
};

/*@}*/
#endif
