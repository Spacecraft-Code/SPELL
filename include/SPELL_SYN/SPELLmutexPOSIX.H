#ifndef __SPELL_MUTEX_POSIX_H__
#define __SPELL_MUTEX_POSIX_H__

// FILES TO INCLUDE ////////////////////////////////////////////////////////
// System includes ---------------------------------------------------------
// Local includes ----------------------------------------------------------
#include "SPELL_SYN/SPELLmutexIF.H"
// Project includes --------------------------------------------------------

/** \addtogroup SPELL_SYN */
/*@{*/


// FORWARD REFERENCES //////////////////////////////////////////////////////
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Mutex implementation for POSIX
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLmutexPOSIX : public SPELLmutexIF
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Constructor.
		**////////////////////////////////////////////////////////////////////
		SPELLmutexPOSIX();

		//////////////////////////////////////////////////////////////////////
		/** Destructor.
		**////////////////////////////////////////////////////////////////////
		~SPELLmutexPOSIX();

	// STATIC ////////////////////////////////////////////////////////////////
	// METHODS ///////////////////////////////////////////////////////////////

		//////////////////////////////////////////////////////////////////////
		/** Lock the mutex
		**////////////////////////////////////////////////////////////////////
		void lock();

		//////////////////////////////////////////////////////////////////////
		/** Lock the mutex
		**////////////////////////////////////////////////////////////////////
		bool trylock();

		//////////////////////////////////////////////////////////////////////
		/** Unlock the mutex
		**////////////////////////////////////////////////////////////////////
		void unlock();

		//////////////////////////////////////////////////////////////////////
		/** Used by POSIX condition class, provides the direct mutex structure
		 * reference.
		 *
		 * \returns Pointer to the mutex structure
		**////////////////////////////////////////////////////////////////////
		pthread_mutex_t* get() { return &m_mutex; };

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////
		/** Holds the mutex data structure */
		pthread_mutex_t m_mutex;
		/** Holds the id of the locking thread */
		pthread_t m_threadId;
};

/*@}*/
#endif
