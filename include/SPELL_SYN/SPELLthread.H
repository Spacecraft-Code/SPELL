#ifndef __SPELL_THREAD_H__
#define __SPELL_THREAD_H__

// FILES TO INCLUDE ////////////////////////////////////////////////////////
// System includes ---------------------------------------------------------
// Local includes ----------------------------------------------------------
#include "SPELL_SYN/SPELLmutex.H"
// Project includes --------------------------------------------------------
#include "SPELL_UTIL/SPELLbase.H"

/** \addtogroup SPELL_SYN */
/*@{*/


// FORWARD REFERENCES //////////////////////////////////////////////////////
// TYPES ///////////////////////////////////////////////////////////////////
// DEFINES /////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Encapsulates a thread mechanism
 **
 ** \par Description: thread mechanism wrapper. The implementation is platform
 ** dependent.
 **
 ** \par Usage: active object may be easily created by inheriting from this
 ** class and implementing the run() method.
 **
**////////////////////////////////////////////////////////////////////////////
class SPELLthread
{
public: //--------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////
    /** Constructor.
    **////////////////////////////////////////////////////////////////////
    SPELLthread( std::string id );

    //////////////////////////////////////////////////////////////////////
    /** Destructor
    **////////////////////////////////////////////////////////////////////
    virtual ~SPELLthread();

    // STATIC ////////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

    //////////////////////////////////////////////////////////////////////
    /** Get thread id
    **////////////////////////////////////////////////////////////////////
    std::string getThreadId() {
        return m_threadId;
    };

    //////////////////////////////////////////////////////////////////////
    /** Start the thread
    **////////////////////////////////////////////////////////////////////
    void start();

    //////////////////////////////////////////////////////////////////////
    /** Block the caller until the thread has finished
    **////////////////////////////////////////////////////////////////////
    void join();

    //////////////////////////////////////////////////////////////////////
    /** Thread work implementation
    **////////////////////////////////////////////////////////////////////
    virtual void run() = 0;

    //////////////////////////////////////////////////////////////////////
    /** Launcher of the implemented thread
    **////////////////////////////////////////////////////////////////////
    void doRun();

    // DATA MEMBERS //////////////////////////////////////////////////////////

protected: //-----------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////

    void setRunning( bool running );
    bool isRunning();

    // DATA MEMBERS //////////////////////////////////////////////////////////

private: //-------------------------------------------------------------------

    // EXCEPTIONS ////////////////////////////////////////////////////////////
    // ENUMS /////////////////////////////////////////////////////////////////
    // TYPES /////////////////////////////////////////////////////////////////
    // LIFECYCLE /////////////////////////////////////////////////////////////
    // METHODS ///////////////////////////////////////////////////////////////
    // DATA MEMBERS //////////////////////////////////////////////////////////
    // Thread identifier
    std::string m_threadId;
    // Holds the thread structure
    pthread_t m_thread;
    pthread_attr_t m_threadAttr;
    // Sync mutex
    SPELLmutex m_threadLock;
    // True if running
    bool m_threadRunning;
};

/*@}*/
#endif
