//////////////////////////////////////////////////////////////////////////////
/**
 ** \brief Obtain a standard string from any python object
 **
**////////////////////////////////////////////////////////////////////////////
#define PYSSTR(x)     SPELLutils::pyObjectString(x) // PYSTR( PyObject_Str(x) )

